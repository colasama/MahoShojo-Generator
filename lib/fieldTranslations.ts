// 获取字段的中文名称，如果没有映射则返回原字段名
export function getFieldDisplayName(fieldName: string): string {
  return FIELD_TRANSLATIONS[fieldName] || fieldName;
}

// 批量翻译对象的键名
export function translateObjectKeys(obj: any): any {
  if (typeof obj !== 'object' || obj === null) {
    return obj;
  }

  if (Array.isArray(obj)) {
    return obj.map(item => translateObjectKeys(item));
  }

  const translatedObj: any = {};
  for (const [key, value] of Object.entries(obj)) {
    const translatedKey = getFieldDisplayName(key);
    translatedObj[translatedKey] = translateObjectKeys(value);
  }
  
  return translatedObj;
}